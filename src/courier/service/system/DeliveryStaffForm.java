    public void setDelivered() {
        Object[] data = new Object[4];
        model_student = (DefaultTableModel) tabDelivered.getModel();
        model_student.getDataVector().removeAllElements();
        try {
            File file = new File("Order.txt");
            try (BufferedReader br = new BufferedReader(new FileReader(file))) {
                String line;
                int i = 0;
                while ((line = br.readLine()) != null) {
                    if (line.contains(LoginForm.UserName) && line.contains("Delivered")) {
                        String[] vec = line.split(",");
                        data[0] = vec[0];
                        data[1] = vec[1];
                        data[2] = vec[2];
                        data[3] = vec[3];
                        model_student.addRow(data);
                    }
                }
                tabDelivered.setModel(model_student);
            }
        } catch (Exception e) {
            System.err.println(e);
        }
    }

