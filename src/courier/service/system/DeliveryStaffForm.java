    private void btnUndeliveredActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUndeliveredActionPerformed
        int column = 0;
        int row = tabUndelivered.getSelectedRow();
        String value = tabUndelivered.getModel().getValueAt(row, column).toString();
        try {
            File file = new File("Order.txt");
            FileReader fr = new FileReader(file);
            try (BufferedReader br = new BufferedReader(new FileReader(file))) {
                String line;
                while ((line = br.readLine()) != null) {
                    if (line.startsWith(value)) {
                        line = line.replace("Undelivered", "Delivered");
                        lines.add(line);
                    }
                }
                fr.close();
                br.close();
                FileWriter fw = new FileWriter(file);
                BufferedWriter out = new BufferedWriter(fw);
                for (String s : lines) {
                    out.write(s);
                }
                out.flush();
                out.close();
                JOptionPane.showMessageDialog(null, "Order Update Successfully");
                setDelivered();
                setUndelivered();
            }
        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_btnUndeliveredActionPerformed

